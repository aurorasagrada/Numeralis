// Função para detectar desafios cármicos baseados na pirâmide
function detectarDesafiosCarmicos(piramide) {
  const desafiosEncontrados = [];
  
  // Analisar números que aparecem com mais frequência na pirâmide
  const contagem = {};
  for (let i = 0; i < piramide.length; i++) {
    for (let j = 0; j < piramide[i].length; j++) {
      const num = piramide[i][j];
      contagem[num] = (contagem[num] || 0) + 1;
    }
  }
  
  // Identificar números que aparecem 3 ou mais vezes como possíveis desafios cármicos
  for (let num in contagem) {
    if (contagem[num] >= 3 && desafiosCarmicosExpandidos[num]) {
      desafiosEncontrados.push({
        numero: parseInt(num),
        frequencia: contagem[num],
        desafio: desafiosCarmicosExpandidos[num]
      });
    }
  }
  
  // Também verificar número do topo da pirâmide como desafio principal
  if (piramide.length > 0 && piramide[0].length > 0) {
    const numeroTopo = piramide[0][0];
    if (desafiosCarmicosExpandidos[numeroTopo]) {
      const jaExiste = desafiosEncontrados.find(d => d.numero === numeroTopo);
      if (!jaExiste) {
        desafiosEncontrados.push({
          numero: numeroTopo,
          frequencia: 1,
          posicao: 'topo',
          desafio: desafiosCarmicosExpandidos[numeroTopo]
        });
      }
    }
  }
  
  return desafiosEncontrados;
}

